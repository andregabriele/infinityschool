numero = float(input("Insira um número: "))

if numero > 0:
    print(f"O número é positivo. Número inserido pelo usuário: {numero}")
elif numero == 0:
    print(f"O número é zero. NNúmero inserido pelo usuário: {numero}")
else:
    print(f"O número é negativo. Número inserido pelo usuário: {numero}")